export const getFestivalsByMonth = (month: number): TempleFestival[] => {
  return templeFestivals.filter(festival => festival.month === month);
};

export const getFestivalsByTemple = (templeId: string): TempleFestival[] => {
  return templeFestivals.filter(festival => festival.templeId === templeId);
};

export const getUpcomingFestivals = (limit: number = 5): TempleFestival[] => {
  const today = new Date();
  return templeFestivals
    .filter(festival => {
      const festivalDate = new Date(2024, festival.month, festival.day);
      return festivalDate >= today;
    })
    .sort((a, b) => {
      const dateA = new Date(2024, a.month, a.day);
      const dateB = new Date(2024, b.month, b.day);
      return dateA.getTime() - dateB.getTime();
    })
    .slice(0, limit);
};